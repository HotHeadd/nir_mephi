/*
Сценарий 4: переполнение соседнего буфера затирает критическую переменную.

Соседство буфера и флага задано явно (поля одной структуры идут подряд),
поэтому запись за границу overflow_buf детерминированно попадает в flag.
Это воспроизводит типовой механизм эксплуатации управляющих данных.

Охраняемый объект — поле flag. Программа печатает его адрес; агент
запускается с этим адресом через --addr. Сценарий собирается как no-pie,
чтобы адрес был стабилен между запусками (см. примечание в тексте о том,
почему в общем случае предпочтительно символическое объявление).
*/
#include <stdint.h>
#include <stdio.h>
#include <string.h>

static struct {
    char     overflow_buf[8];
    uint64_t flag;
} blk;

int main(void) {
    blk.flag = 0;
    // печатаем адрес поля, чтобы передать его агенту через --addr
    printf("[s4] адрес flag = %p\n", (void*)&blk.flag);
    printf("[s4] flag до переполнения = 0x%lx\n", (unsigned long)blk.flag);

    // запись на 8 байт за границу overflow_buf — затирает blk.flag
    memset(blk.overflow_buf, 0x41, sizeof(blk.overflow_buf) + 8);

    printf("[s4] flag после переполнения = 0x%lx\n", (unsigned long)blk.flag);
    return 0;
}
