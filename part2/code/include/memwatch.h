/*
memwatch — опытный образец модуля перехвата событий доступа к памяти.
Общие типы и конфигурация. Этап 2 НИР.

Назначение: минимально работоспособный прототип для проверки гипотез
о работоспособности связки ptrace + mprotect и о достижимости атрибуции
до инструкции. НЕ полнофункциональный инструмент.

Допущения прототипа (унаследованы из методики, глава 1):
  - однопоточная цель;
  - режим launch (агент порождает цель);
  - контроль записи (W): охраняемая область переводится в R-only;
  - реакция = регистрация события с атрибуцией, без пресечения.

Различение легитимного доступа: запись из функций белого списка
(allow_funcs) считается легитимной и не регистрируется как нарушение.
Опирается на атрибуцию инициатора (см. attrib).
*/
#pragma once

#include <stddef.h>
#include <stdint.h>
#include <sys/types.h>

// Описание охраняемой области, как её задаёт пользователь.
typedef struct {
    const char *symbol_name;   // символическое имя (приоритетный способ)
    uintptr_t   abs_addr;      // либо абсолютный адрес (fallback), 0 если не задан
    size_t      length;        // размер охраняемой области в байтах
} guard_spec_t;

// Конфигурация запуска агента.
typedef struct {
    char       **target_argv;  // argv для execve целевой программы
    guard_spec_t guard;        // что охраняем
    const char  *log_path;     // путь к журналу событий, NULL = stderr
    char       **allow_funcs;  // белый список функций-инициаторов (NULL-терм.)
    int          allow_count;  // число записей в allow_funcs
} agent_config_t;

// Разрешённая в рантайме охраняемая область.
typedef struct {
    uintptr_t addr;            // фактический виртуальный адрес начала
    size_t    length;          // размер
    size_t    page_addr;       // адрес страницы (addr, выровненный вниз)
    size_t    page_span;       // сколько байт страниц покрывает область
} guard_region_t;
